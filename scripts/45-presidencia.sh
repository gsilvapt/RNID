#!/bin/bash

if [ "$(wget https://www.presidencia.pt/ -o -|grep -c ERROR)" -eq "0" ];  then
	echo "presidencia: incumprimento pode já não existir";
else
	echo "presidencia: Incumprimento mantém-se, a actualizar o README (faça um git diff, valide, e commit!)";
	while IFS='' read -r line || [[ -n "$line" ]]; do
		test "$(echo "$line"|grep -v -c "presidencia")" -eq "1" \
			&& echo "$line" \
			|| (h=$(echo "$line"|cut -d\| -f1-4); t=$(echo "$line"|cut -d\| -f6-); echo "$h| $(date +%Y/%m/%d) |$t");
	done < README.md > new
	mv new README.md
fi
